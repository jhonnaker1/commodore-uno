#!/usr/bin/env python3
"""Generate an 8x8 bitmap font header for the Atari ST/STE UNO port.

The ST has no text mode and its ROM font is only reachable through Line-A /
VDI, so the port carries its own font. Glyphs are authored here as 5x7
ASCII art (padded into an 8x8 cell) because that is far easier to verify by
eye than raw hex, then emitted as a C array indexed by (ascii - 32).
"""

